//Verbo ++ Substantivo
let nome = 'Locas';


function MudarNome(Pnome, Snome, Unome){
    nome = Pnome + ' ' + Snome + ' ' + Unome;
}

console.log(nome)
MudarNome('Igor', 'Dantas', 'Soares');
console.log(nome)

